    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int indiceSelecionado = cbCargo.getSelectedIndex();
        // Chama o método definir comissão passando o índice selecionado
        
        if(controlador.verificar()){
            
            Funcionario funcionario = controlador.getFuncionario();
            controlador.definirComissao(funcionario);
            
            funcionario.setIdUsuario(1);
            controlador.alterar(funcionario);
            
            JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso!");
        }
        else {
            JOptionPane.showMessageDialog(this, "É necessário informar os campos de forma adequada!");
        }
        
        controlador.limpar(new JTextField[] {nome, login, senha, salario});
    }//GEN-LAST:event_jButton1ActionPerformed

